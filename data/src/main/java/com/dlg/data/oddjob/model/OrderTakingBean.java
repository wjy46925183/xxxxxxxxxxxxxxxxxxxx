public class OrderTakingBean {

    /**
     * postName : 物流员
     * postType : job.type_16
     * postTypeName : 其他
     * demandType : 3
     * price : 1
     * meterUnit : null
     * meterUnitName : null
     * startDate : null
     * endDate : null
     * totalPrice : 1
     */

    private String postName; //名称
    private String postType; //类型
    private String postTypeName; //类型名称
    private int demandType; //需求类型(1.工作日,2.双休日,3.计件)
    private double price; //单价
    private String meterUnit; //几天或几小时
    private String meterUnitName; ////单位
    private String startDate; //开始时间
    private String endDate; //结束时间
    private double totalPrice; //总价格

    public String getPostName() {
        return postName;
    }

    public void setPostName(String postName) {
        this.postName = postName;
    }

    public String getPostType() {
        return postType;
    }

    public void setPostType(String postType) {
        this.postType = postType;
    }

    public String getPostTypeName() {
        return postTypeName;
    }

    public void setPostTypeName(String postTypeName) {
        this.postTypeName = postTypeName;
    }

    public int getDemandType() {
        return demandType;
    }

    public void setDemandType(int demandType) {
        this.demandType = demandType;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getMeterUnit() {
        return meterUnit;
    }

    public void setMeterUnit(String meterUnit) {
        this.meterUnit = meterUnit;
    }

    public String getMeterUnitName() {
        return meterUnitName;
    }

    public void setMeterUnitName(String meterUnitName) {
        this.meterUnitName = meterUnitName;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public double getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(double totalPrice) {
        this.totalPrice = totalPrice;
    }
}
